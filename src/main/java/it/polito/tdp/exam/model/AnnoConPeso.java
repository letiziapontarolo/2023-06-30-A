package it.polito.tdp.exam.model;

import java.util.Collections;
import java.util.Comparator;

public class AnnoConPeso {
	
	int anno;
	double peso;
	public AnnoConPeso(int anno, double peso) {
		this.anno = anno;
		this.peso = peso;
	}
	public int getAnno() {
		return anno;
	}
	public void setAnno(int anno) {
		this.anno = anno;
	}
	public double getPeso() {
		return peso;
	}
	public void setPeso(double peso) {
		this.peso = peso;
	}
	
	

}
