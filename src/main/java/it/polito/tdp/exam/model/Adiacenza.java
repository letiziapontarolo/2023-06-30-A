package it.polito.tdp.exam.model;

public class Adiacenza {
	
	int y1;
	int y2;
	int peso;
	public Adiacenza(int y1, int y2) {
		super();
		this.y1 = y1;
		this.y2 = y2;
	}
	public int getY1() {
		return y1;
	}
	public void setY1(int y1) {
		this.y1 = y1;
	}
	public int getY2() {
		return y2;
	}
	public void setY2(int y2) {
		this.y2 = y2;
	}
	public int getPeso() {
		return peso;
	}
	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	

}
